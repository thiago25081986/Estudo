# Tipos int e Float
#int -> numeros inteiros 
# O tipo int reprsenta qualquer numero positivo 
# ou negativo. int sem sinal é considerado 
# positivo
# print(11) #int
# print(-11) #int
# print (0) #int

# # Float -> Numero com ponto flutuante 
# # O tipo float representa qualquer numero 
# #positivo ou negativo com ponto flutuante (casas decimais). 
# # Float sem sinal é considerado positivo. OBS: sempre usar . para separar as casas decimais
# print(1.1, 10.11)
# print(0.0, -1.5)
# A função type mostra o tipo que o Python 
# inferiu ao valor. 
print( type('thiago'))
print( type(0))
print(type (1.1), type(-1.1), type(0.))
